    <script>
        $(function() {

            function reloadCaptcha() {
                $("#captchax").attr("src", "<?php echo e(route('sites.captcha')); ?>?r=" + Math.random());
            }
            $('.captcode').click(function(e) {
                e.preventDefault();
                reloadCaptcha();
            });

            function swapButton() {
                var txtswap2 = $("#text-of-sub-btn");
                if (txtswap2.hasClass('sending')) {
                    txtswap2.removeClass('sending');
                    txtswap2.html(txtswap2.data("btntext-original"));
                } else {
                    txtswap2.addClass('sending');
                    txtswap2.html(txtswap2.data("btntext-sending"));
                }
            }

            $("#smart-form").validate({

                /* @validation states + elements 
                ------------------------------------------- */
                errorClass: "is-invalid",
                validClass: "is-valid",
                errorElement: "span",
                onkeyup: true,
                onclick: true,
                onsubmit: true,

                /* @validation rules 
                ------------------------------------------ */
                rules: {
                    sendername: {
                        required: true,
                        minlength: 2
                    },
                    emailaddress: {
                        required: true,
                        email: true
                    },
                    telephone: {
                        required: true,
                        minlength: 6,
                        maxlength: 12
                    },
                    sendersubject: {
                        required: true,
                        minlength: 4
                    },
                    sendermessage: {
                        required: true,
                        minlength: 10
                    },
                    captcha: {
                        required: true,
                        remote: "<?php echo e(route('sites.captcha_process')); ?>"
                    }
                },
                messages: {
                    sendername: {
                        required: 'Enter your name',
                        minlength: 'Name must be at least 2 characters'
                    },
                    emailaddress: {
                        required: 'Enter your email address',
                        email: 'Enter a VALID email address'
                    },
                    telephone: {
                        required: 'Enter your Telephone',
                        minlength: 'Telephone must be at least 6 characters',
                        maxlength: 'Telephone must be at max 12 characters'
                    },
                    sendersubject: {
                        required: 'Subject is important',
                        minlength: 'Subject must be at least 4 characters'
                    },
                    sendermessage: {
                        required: 'Oops you forgot your message',
                        minlength: 'Message must be at least 10 characters'
                    },
                    captcha: {
                        required: 'You must enter the captcha code',
                        remote: 'Captcha code is incorrect'
                    }
                },

                /* @validation highlighting + error placement  
                ---------------------------------------------------- */
                highlight: function(element, errorClass, validClass) {
                    $(element).closest('.form-control').addClass(errorClass).removeClass(validClass);
                },
                unhighlight: function(element, errorClass, validClass) {
                    $(element).closest('.form-control').removeClass(errorClass).addClass(validClass);
                },
                errorPlacement: function(error, element) {
                    element.closest('.mb-30px').find('.field').addClass('error fs-14').html(error);
                },

                /* @ajax form submition 
                ---------------------------------------------------- */
                submitHandler: function(form, errorClass, validClass) {
                    $(form).ajaxSubmit({
                        method: 'post',
                        target: '.result',
                        beforeSubmit: function() {
                            swapButton();
                        },
                        error: function() {
                            swapButton();
                        },
                        success: function() {
                            swapButton();
                            $('.alert-success').show().delay(7000).fadeOut();
                            $('.form-control').removeClass(errorClass);
                            $('.form-control').removeClass(validClass);
                            if ($('.alert-error').length == 0) {
                                $('#smart-form').resetForm();
                                reloadCaptcha();
                            }
                        }
                    });
                }

            });

        });
    </script>

